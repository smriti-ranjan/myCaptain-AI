#Loops

#For and While
         
i=20
while i<=40:
    print(i)
    i=i+2
print("While loop stopped")

for x in range(1,11):
    print(x)
print("For loop stopped")

fruits = ['apples','oranges','mangoes','grapes']

for x in fruits:
    print(x)
print("For loop stopped")


n = int(input("Enter a number: "))
i=0
while i<=n:
    print(i)
    i+=1
